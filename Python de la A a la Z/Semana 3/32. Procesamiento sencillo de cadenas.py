variable = ' Fernanda '
# Todos los caracteres en MAYÚSCULAS'
print(variable.upper())

#Solo la primera en MAYÚSCULA'
print(variable.capitalize())

#Todos los caracteres en minúscula'
print(variable.lower())

#Eliminar espacios basura del string'
print(variable.strip())

#Remplazar caracter
print(variable.replace('a', 'i'))
